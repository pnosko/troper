                <div id="main-articleSource" class="articleSource-content retro-folders">
                    <p>If a show features two dumb or ditzy characters, there are high chances they will be paired
                        together in a romantic relationship, often for comedic reasons or because <a class="twikilink"
                            href="/pmwiki/pmwiki.php/Main/StrangeMindsThinkAlike"
                            title="/pmwiki/pmwiki.php/Main/StrangeMindsThinkAlike" data-ss1551747943="1">Strange Minds
                            Think Alike</a>
                    </p>
                    <p>Sometimes it happens because the token dumb character needs a love interest, so why not introduce
                        a possible love interest who is just as dumb or even dumber than him? <a class="twikilink"
                            href="/pmwiki/pmwiki.php/Main/HilarityEnsues" title="/pmwiki/pmwiki.php/Main/HilarityEnsues"
                            data-ss1551747943="1">Hilarity Ensues</a>. Very often, the two token idiots are minor
                        characters who are just there to form a funny <a class="twikilink"
                            href="/pmwiki/pmwiki.php/Main/BetaCouple" title="/pmwiki/pmwiki.php/Main/BetaCouple"
                            data-ss1551747943="1">Beta Couple</a>. Sometimes it may happen that they break up very soon
                        because they are <a class="twikilink" href="/pmwiki/pmwiki.php/Main/TooMuchAlike"
                            title="/pmwiki/pmwiki.php/Main/TooMuchAlike" data-ss1551747943="1">Too Much Alike</a>.
                    </p>
